package Day8;

public class chanel {

	public static int chaenel =0;
	void  chaenelup() {
		for (int i=1; i<=10; i++) {
			chaenel++;
			System.out.println("채널"+chaenel);
		
		}
		
	}
	void chaeneldown() {
		for (int i=1; i<=9; i++) {
			chaenel--;
			System.out.println("채널"+chaenel);
		
		}
	}
	
	
}
